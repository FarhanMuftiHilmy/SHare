class Users {
    constructor(id, firstName, lastName, email, password, age, phoneNumber, gender ) {
            this.id = id;
            this.firstName = firstName;
            this.lastName = lastName;
            this.email = email;
            this.password = password;
            this.age = age;
            this.phoneNumber = phoneNumber;
            this.gender = gender;
    }
}

module.exports = Users;